function tambah (x, y){
    let hasil = x + y;
    console.log("Hasil "+x+" + "+y+" = "+ hasil);
}
function kurang (x, y){
    let hasil = x - y;
    console.log("Hasil "+x+" - "+y+" = "+ hasil);
}
function kali (x, y){
    let hasil = x * y;
    console.log("Hasil "+x+" x "+y+" = "+ hasil);
}
function bagi (x, y){
    let hasil = x / y;
    console.log("Hasil "+x+" - "+y+" = "+ hasil);
}
function mod (x, y){
    let hasil = x % y;
    console.log("Hasil "+x+" % "+y+" = "+ hasil);
}

tambah(2,3)
kurang(4,1)
kali(2,3)
bagi(10,5)
mod(5,3)
